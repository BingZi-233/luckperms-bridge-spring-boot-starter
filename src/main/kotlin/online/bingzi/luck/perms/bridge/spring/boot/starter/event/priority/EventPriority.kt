package online.bingzi.luck.perms.bridge.spring.boot.starter.event.priority

/**
 * 事件优先级枚举类
 * 
 * 该枚举类定义了事件的优先级等级，主要用于在事件处理系统中对事件进行排序和管理。
 * 不同的优先级可用于控制事件的处理顺序，以确保高优先级的事件能够优先得到处理。
 */
enum class EventPriority {
    /**
     * 最低优先级
     * 表示该事件的优先级最低，处理时将会被其他更高优先级的事件所延后。
     */
    LOWEST,

    /**
     * 较低优先级
     * 表示该事件的优先级低于正常优先级，但高于最低优先级。
     */
    LOW,

    /**
     * 正常优先级
     * 表示该事件的优先级为默认优先级，通常适用于大多数事件。
     */
    NORMAL,

    /**
     * 较高优先级
     * 表示该事件的优先级高于正常优先级，适用于需要快速响应的事件。
     */
    HIGH,

    /**
     * 最高优先级
     * 表示该事件的优先级最高，处理时将优先于所有其他事件。
     */
    HIGHEST
} 