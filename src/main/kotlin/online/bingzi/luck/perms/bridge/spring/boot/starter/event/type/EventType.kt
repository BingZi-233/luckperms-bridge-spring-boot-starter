package online.bingzi.luck.perms.bridge.spring.boot.starter.event.type

/**
 * 事件类型枚举
 * 
 * 该枚举定义了不同的事件类型，用于在应用程序中进行事件的分类和处理。
 * 每种事件类型代表了一种特定的事件，便于在事件驱动架构中进行管理和响应。
 */
enum class EventType {
    /**
     * 日志广播事件
     * 该事件用于表示日志信息的广播，通常用于日志收集和分发。
     */
    LOG_BROADCAST,

    /**
     * 网络同步前事件
     * 该事件在进行网络同步之前触发，允许在同步开始之前执行某些操作。
     */
    PRE_NETWORK_SYNC,

    /**
     * 网络同步后事件
     * 该事件在网络同步完成后触发，允许在同步结束后执行后续操作。
     */
    POST_NETWORK_SYNC,

    /**
     * 同步前事件
     * 该事件在进行数据同步之前触发，适用于准备工作，例如数据验证或预处理。
     */
    PRE_SYNC,

    /**
     * 同步后事件
     * 该事件在数据同步完成后触发，适合处理同步结果或进行后续逻辑。
     */
    POST_SYNC,

    /**
     * 自定义消息事件
     * 该事件用于处理用户自定义的消息，可以用于实现特定业务逻辑。
     */
    CUSTOM_MESSAGE,

    /**
     * 连接状态事件
     * 该事件用于表示连接状态的变化，例如连接建立、断开等。
     */
    CONNECTION_STATE
}